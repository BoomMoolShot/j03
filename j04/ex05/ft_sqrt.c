int		ft_sqrt(int nb)
{
	long long int	res;

	res = nb / 3 + 1;
	while (res * res > nb)
	{
		res = (nb / res + res) / 2;
	}
	if (res * res != nb)
	{
		res = 0;
	}
	return (res);
}
